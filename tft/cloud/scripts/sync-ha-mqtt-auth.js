#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const crypto = require("crypto");
const { spawnSync } = require("child_process");

const ROOT = path.join(__dirname, "..");
const DB_PATH = process.env.WMS_DB_PATH || path.join(ROOT, "storage", "db.json");
const PASSWD_FILE = process.env.WMS_HA_PASSWD_FILE || "/etc/mosquitto/passwd";
const ACL_FILE = process.env.WMS_HA_ACL_FILE || "/etc/mosquitto/wms-ha.acl";
const TOPIC_PREFIX = String(process.env.WMS_MQTT_TOPIC_PREFIX || "wms").trim() || "wms";
const DISCOVERY_PREFIX = String(process.env.WMS_HA_DISCOVERY_PREFIX || "homeassistant").trim() || "homeassistant";
const USER_PREFIX = String(process.env.WMS_HA_USER_PREFIX || "ha_").trim() || "ha_";
const SERVICE_USERS = String(process.env.WMS_MQTT_SERVICE_USERS || process.env.MQTT_USERNAME || "wms_device")
  .split(",")
  .map((x) => x.trim())
  .filter(Boolean);
const MOSQUITTO_PASSWD_BIN = process.env.WMS_MOSQUITTO_PASSWD_BIN || "mosquitto_passwd";

function fail(msg) {
  console.error(`[sync-ha-mqtt-auth] ERROR: ${msg}`);
  process.exit(1);
}

function sanitizeMqttPart(s) {
  const raw = String(s || "").trim().toLowerCase();
  if (!raw) return "wm";
  const normalized = raw
    .replace(/[^a-z0-9_-]+/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "");
  return normalized || "wm";
}

function randomPassword(len = 24) {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnpqrstuvwxyz23456789";
  const bytes = crypto.randomBytes(Math.max(8, len));
  let out = "";
  for (let i = 0; i < len; i++) out += alphabet[bytes[i] % alphabet.length];
  return out;
}

function readDb(filePath) {
  if (!fs.existsSync(filePath)) return { users: [], devices: {} };
  const raw = fs.readFileSync(filePath, "utf8");
  const parsed = JSON.parse(raw);
  if (!parsed.devices || typeof parsed.devices !== "object") parsed.devices = {};
  if (!Array.isArray(parsed.users)) parsed.users = [];
  return parsed;
}

function writeDb(filePath, db) {
  fs.writeFileSync(filePath, JSON.stringify(db, null, 2));
}

function ensureHaCreds(db) {
  let changed = false;
  const entries = [];
  for (const [deviceId, metaRaw] of Object.entries(db.devices || {})) {
    const meta = metaRaw && typeof metaRaw === "object" ? metaRaw : {};
    db.devices[deviceId] = meta;
    if (!meta.ownerUserId) continue;

    const username = `${USER_PREFIX}${sanitizeMqttPart(deviceId)}`;
    const nowIso = new Date().toISOString();
    const current = meta.haMqtt && typeof meta.haMqtt === "object" ? meta.haMqtt : {};
    const next = {
      username,
      password: String(current.password || ""),
      discoveryNode: String(current.discoveryNode || deviceId),
      createdAt: String(current.createdAt || nowIso),
      rotatedAt: String(current.rotatedAt || current.createdAt || nowIso),
      revision: Number(current.revision || 1),
    };

    let localChanged = false;
    if (current.username !== username) localChanged = true;
    if (!next.password) {
      next.password = randomPassword(24);
      next.rotatedAt = nowIso;
      next.revision = Number.isFinite(Number(current.revision)) ? Number(current.revision) + 1 : 1;
      localChanged = true;
    }
    if (!next.discoveryNode || next.discoveryNode !== deviceId) {
      next.discoveryNode = deviceId;
      localChanged = true;
    }
    if (!current.createdAt) localChanged = true;

    if (localChanged) {
      meta.haMqtt = next;
      changed = true;
    } else {
      meta.haMqtt = current;
    }

    entries.push({
      deviceId,
      username: meta.haMqtt.username,
      password: meta.haMqtt.password,
      discoveryNode: meta.haMqtt.discoveryNode || deviceId,
    });
  }

  entries.sort((a, b) => String(a.deviceId).localeCompare(String(b.deviceId), "en"));
  return { changed, entries };
}

function runCommand(bin, args) {
  const result = spawnSync(bin, args, { encoding: "utf8" });
  if (result.error) {
    fail(`${bin} not available: ${result.error.message}`);
  }
  if (result.status !== 0) {
    const stderr = String(result.stderr || "").trim();
    const stdout = String(result.stdout || "").trim();
    fail(`${bin} ${args.join(" ")} failed (${result.status}) ${stderr || stdout}`);
  }
}

function buildAcl(entries) {
  const lines = [];
  lines.push("# AUTOGENERATED by cloud/scripts/sync-ha-mqtt-auth.js");
  lines.push("# Do not edit manually. Changes will be overwritten.");
  lines.push("");
  for (const serviceUser of SERVICE_USERS) {
    lines.push(`user ${serviceUser}`);
    lines.push(`topic readwrite ${TOPIC_PREFIX}/#`);
    lines.push(`topic readwrite ${DISCOVERY_PREFIX}/#`);
    lines.push("");
  }
  for (const e of entries) {
    lines.push(`user ${e.username}`);
    lines.push(`topic read ${TOPIC_PREFIX}/${e.deviceId}/#`);
    lines.push(`topic write ${TOPIC_PREFIX}/${e.deviceId}/cmd/#`);
    lines.push(`topic write ${TOPIC_PREFIX}/${e.deviceId}/global/refresh`);
    lines.push(`topic read ${DISCOVERY_PREFIX}/+/${e.discoveryNode}/#`);
    lines.push(`topic read ${DISCOVERY_PREFIX}/status`);
    lines.push(`topic write ${DISCOVERY_PREFIX}/status`);
    lines.push("");
  }
  return lines.join("\n");
}

function ensureParent(filePath) {
  const dir = path.dirname(filePath);
  fs.mkdirSync(dir, { recursive: true });
}

function resolveGroupGid(groupName) {
  try {
    const content = fs.readFileSync("/etc/group", "utf8");
    for (const line of content.split(/\r?\n/)) {
      if (!line || line.startsWith("#")) continue;
      const parts = line.split(":");
      if (parts.length < 3) continue;
      if (parts[0] === groupName) {
        const gid = Number(parts[2]);
        if (Number.isInteger(gid) && gid >= 0) return gid;
      }
    }
  } catch (_) {}
  return null;
}

function hardenMosquittoFile(filePath, mode) {
  fs.chmodSync(filePath, mode);
  if (typeof process.getuid === "function" && process.getuid() === 0) {
    const gid = resolveGroupGid("mosquitto");
    if (gid !== null) {
      fs.chownSync(filePath, 0, gid);
    }
  }
}

function writeMosquittoFiles(entries) {
  ensureParent(PASSWD_FILE);
  ensureParent(ACL_FILE);

  const passwdTmp = `${PASSWD_FILE}.tmp-${process.pid}`;
  const aclTmp = `${ACL_FILE}.tmp-${process.pid}`;

  try {
    const preserved = [];
    if (fs.existsSync(PASSWD_FILE)) {
      const lines = fs.readFileSync(PASSWD_FILE, "utf8").split(/\r?\n/);
      for (const line of lines) {
        if (!line.trim()) continue;
        const colon = line.indexOf(":");
        if (colon <= 0) continue;
        const username = line.slice(0, colon).trim();
        if (username.startsWith(USER_PREFIX)) continue; // replace all HA users
        preserved.push(line);
      }
    }

    fs.writeFileSync(passwdTmp, preserved.join("\n") + (preserved.length ? "\n" : ""), { mode: 0o600 });

    let hasUsers = preserved.length > 0;
    for (const e of entries) {
      const args = hasUsers
        ? ["-b", passwdTmp, e.username, e.password]
        : ["-b", "-c", passwdTmp, e.username, e.password];
      runCommand(MOSQUITTO_PASSWD_BIN, args);
      hasUsers = true;
    }
    fs.chmodSync(passwdTmp, 0o600);

    fs.writeFileSync(aclTmp, buildAcl(entries), { mode: 0o640 });
    fs.renameSync(passwdTmp, PASSWD_FILE);
    fs.renameSync(aclTmp, ACL_FILE);
    hardenMosquittoFile(PASSWD_FILE, 0o640);
    hardenMosquittoFile(ACL_FILE, 0o640);
  } finally {
    if (fs.existsSync(passwdTmp)) fs.unlinkSync(passwdTmp);
    if (fs.existsSync(aclTmp)) fs.unlinkSync(aclTmp);
  }
}

function main() {
  if (!fs.existsSync(DB_PATH)) fail(`DB file not found: ${DB_PATH}`);

  const db = readDb(DB_PATH);
  const { changed, entries } = ensureHaCreds(db);
  if (changed) writeDb(DB_PATH, db);

  writeMosquittoFiles(entries);

  console.log("[sync-ha-mqtt-auth] done");
  console.log(`[sync-ha-mqtt-auth] db: ${DB_PATH}`);
  console.log(`[sync-ha-mqtt-auth] passwd: ${PASSWD_FILE}`);
  console.log(`[sync-ha-mqtt-auth] acl: ${ACL_FILE}`);
  console.log(`[sync-ha-mqtt-auth] users: ${entries.length}`);
}

main();
